#!/usr/bin/env node
// Regenerate shared/protocol.ts from shared/bindings/*.ts + the hand-maintained
// unions template at shared/protocol.unions.ts.tmpl.
//
// Workflow:
//   1. `cd engine && cargo test` to refresh shared/bindings/*.ts via ts-rs
//      (the literal `type` annotations on each msg_type field are what make
//      this output union-narrow-able from the TS side).
//   2. Run this script to splice the bindings + unions into shared/protocol.ts.
//
// scripts/regen-protocol.sh wraps both steps for convenience.

import { readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const BINDINGS_DIR = join(REPO_ROOT, 'shared', 'bindings');
const UNIONS_TMPL = join(REPO_ROOT, 'shared', 'protocol.unions.ts.tmpl');
const OUT = join(REPO_ROOT, 'shared', 'protocol.ts');

// ts-rs prepends each binding file with this generated-by header. Strip it —
// shared/protocol.ts gets its own header explaining the consolidated layout.
const TS_RS_HEADER = /^\/\/ This file was generated by \[ts-rs\][^\n]*\n+/;

// Each binding ALSO has `import type { Foo } from "./Foo";` lines for the
// other types it references. When everything is concatenated into one file
// those imports point to non-existent siblings AND collide with the inlined
// definitions — strip them. tsc only reports the breakage from `tsc -b`,
// not `tsc --noEmit` with the apps' `files: []` root tsconfig, so this gap
// went unnoticed until the Railway CI build (which uses `npm run build`
// → `tsc -b --force && vite build`).
const TS_RS_IMPORT = /^import type \{[^}]+\} from "\.\/[^"]+";\s*\n/gm;

const files = readdirSync(BINDINGS_DIR)
  .filter((f) => f.endsWith('.ts'))
  .sort(); // deterministic ordering

if (files.length === 0) {
  console.error(
    'no bindings found at shared/bindings/ — run `cd engine && cargo test` first',
  );
  process.exit(1);
}

const HEADER = `// AUTO-GENERATED from shared/bindings/*.ts + shared/protocol.unions.ts.tmpl.
// Do not edit by hand — re-run scripts/regen-protocol.sh after changing
// either engine/engine-core/src/protocol.rs or
// shared/protocol.unions.ts.tmpl.
//
// The per-type interfaces below come from ts-rs (\`#[derive(TS)]\` in the
// Rust source). The unions and aliases at the bottom come from the
// template file because ts-rs can't emit cross-struct discriminated
// unions.

`;

const parts = [HEADER];

for (const file of files) {
  const raw = readFileSync(join(BINDINGS_DIR, file), 'utf8');
  const stripped = raw.replace(TS_RS_HEADER, '').replace(TS_RS_IMPORT, '');
  parts.push(stripped.trim() + '\n');
}

parts.push('\n');
parts.push(readFileSync(UNIONS_TMPL, 'utf8').trim() + '\n');

writeFileSync(OUT, parts.join('\n'));
console.log(
  `wrote ${OUT.replace(REPO_ROOT + '/', '')} — ${files.length} bindings + unions template`,
);
